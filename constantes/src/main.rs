fn main(){
    /* Con respecto al nombre de las constantes,
    por convención deben ser escritas en Mayúscula,
    y es obligatorio indicar el tipo de dato en la definición.*/
    // La palabra reservada para definir una constante son dos: const y static.
	const VALOR: i32 = 15;
    println!("El valor de la variable es: {}", VALOR);
}